#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	if (!s || fd < 0)
		return ;
	while (*s)
	{
		ft_putchar_fd(*s++, fd);
		if (ft_check_ifwrite_failed())
			return ;
	}
}
